/**
 * Транслитерация кириллицы в латиницу
 * Используется для генерации slug из русских названий
 */

const transliterationMap: Record<string, string> = {
  'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd',
  'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
  'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n',
  'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
  'у': 'u', 'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch',
  'ш': 'sh', 'щ': 'sch', 'ъ': '', 'ы': 'y', 'ь': '',
  'э': 'e', 'ю': 'yu', 'я': 'ya',
  'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D',
  'Е': 'E', 'Ё': 'Yo', 'Ж': 'Zh', 'З': 'Z', 'И': 'I',
  'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M', 'Н': 'N',
  'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T',
  'У': 'U', 'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch',
  'Ш': 'Sh', 'Щ': 'Sch', 'Ъ': '', 'Ы': 'Y', 'Ь': '',
  'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
  '№': 'no',
};

/**
 * Транслитерирует кириллический текст в латиницу
 * @param text - Текст для транслитерации
 * @returns Транслитерированный текст
 */
export function transliterate(text: string): string {
  if (!text) return '';
  
  return text
    .split('')
    .map(char => transliterationMap[char] || char)
    .join('');
}

/**
 * Генерирует slug из текста с поддержкой кириллицы
 * @param text - Исходный текст
 * @returns URL-safe slug
 */
export function generateSlug(text: string): string {
  if (!text) return '';
  
  // Сначала транслитерируем
  const transliterated = transliterate(text);
  
  // Затем приводим к lowercase и заменяем пробелы/спецсимволы на дефисы
  return transliterated
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '') // Удаляем все спецсимволы кроме букв, цифр, пробелов и дефисов
    .replace(/\s+/g, '-')          // Заменяем пробелы на дефисы
    .replace(/-+/g, '-')          // Заменяем множественные дефисы на одинарные
    .replace(/^-+/, '')           // Удаляем дефисы в начале
    .replace(/-+$/, '');          // Удаляем дефисы в конце
}
